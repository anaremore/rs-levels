assert.doesNotMatch(pine, /showStatus/);
assert.doesNotMatch(pine, /drawStatus/);
assert.doesNotMatch(pine, /statusText/);
assert.match(pine, /fontSizeInput = input\.string\("Small", "Font size"/);
assert.match(pine, /options = \["Tiny", "Small", "Normal", "Large", "Huge"\]/);
assert.match(pine, /labelSize/);
assert.match(pine, /"Tiny" => size\.tiny/);
assert.match(pine, /"Huge" => size\.huge/);
assert.match(pine, /size = labelSize\(\)/);
assert.match(pine, /displayLabelName/);
assert.match(pine, /str\.contains\(upperName, "OPEN"\)/);

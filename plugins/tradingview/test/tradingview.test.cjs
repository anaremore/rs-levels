const assert = require('node:assert/strict');
const { readFileSync } = require('node:fs');
const { join } = require('node:path');

const pine = readFileSync(join(__dirname, '..', 'rs-levels.pine'), 'utf8');

assert.match(pine, /indicator\("RS Levels"/);
assert.doesNotMatch(pine, /\bstrategy\s*\(/i);
assert.doesNotMatch(pine, /\bstrategy\./i);
assert.doesNotMatch(pine, new RegExp('\\b' + 'ord' + 'er' + '\\b', 'i'));
assert.doesNotMatch(pine, /\balertcondition\s*\(/i);
assert.doesNotMatch(pine, /\brequest\./i);
assert.doesNotMatch(pine, /^\s*text\s*=/m);
assert.match(pine, /labelText = name/);
assert.match(pine, /labelBarsOffset/);
assert.match(pine, /labelVerticalOffset/);
assert.match(pine, /labelDirection/);
assert.match(pine, /labelPrice/);
assert.match(pine, /labelY = labelPrice\(name, kind, price\)/);
assert.doesNotMatch(pine, /label\.new\(bar_index \+ labelBarsOffset, price,/);
assert.match(pine, /RSLEVELS/);
assert.match(pine, /array\.get\(parts, 1\) == "2"/);
assert.match(pine, /chartPayloadSymbol/);
assert.match(pine, /syminfo\.root/);
assert.match(pine, /while sectionIndex \+ 2 < array\.size\(parts\)/);
assert.match(pine, /fallbackSymbol != ""/);
assert.match(pine, /str\.split/);
assert.match(pine, /showStatus/);
assert.match(pine, /showDdBands/);
assert.match(pine, /showHp/);
assert.match(pine, /showBullZones/);
assert.match(pine, /showBearZones/);
assert.match(pine, /"zone-bull"/);
assert.match(pine, /"zone-bear"/);
assert.match(pine, /lineStyleInput/);
assert.match(pine, /maxLevelsInput/);
assert.match(pine, /max_lines_count = 500/);
assert.match(pine, /max_labels_count = 500/);
assert.match(pine, /maxval = 500/);
assert.match(pine, /label\.style_label_down/);

console.log('TradingView plugin tests passed');
